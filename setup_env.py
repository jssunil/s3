    try:
        with open('.env', 'w') as f:
            f.write(env_content)
        
        print("\nâœ… .env file created successfully!")
        
        if not api_key:
            print("\nâš ï¸  Note: You didn't provide a Gemini API key.")
            print("   AI suggestions won't work until you add your API key to the .env file.")
        else:
            print("\nğŸ‰ Your environment is ready!")
            print("   You can now run the application with: python app.py")
        
        print(f"\nğŸ“ .env file location: {os.path.abspath('.env')}")
        
    except Exception as e:
        print(f"\nâŒ Error creating .env file: {e}")
